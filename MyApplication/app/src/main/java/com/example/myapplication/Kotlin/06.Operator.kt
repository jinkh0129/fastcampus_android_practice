//06. 연산자
/*
<연산자의 종류>
1. 산술연산자
 --> +(더하기) -(빼기) *(곱하기) /(몫만 취함) %(나머지만 취함)
2. 대입연산자(=)
 --> 좌변 = 우변 (우변의 값이 좌변에 들어간다)
    ex) var num = 20;
3. 복합 대입 연산자
 --> += -= *= /= %=
    ex) a+=10 (a에 10을 더한 값이 a에 다시 들어가게 된다) --> a = a + 10과 동일하다.
4. 증감 연산자(1을 더하거나 뺄 때 사용한다)
 --> ++ --
    ex) a++는 1을 더한다, a--는 1을 빼준다.
5. 비교 연산자
 --> > < <= >= == !=
6. 논리 연산자
 --> &&(and) ||(or) !(not)
 */
